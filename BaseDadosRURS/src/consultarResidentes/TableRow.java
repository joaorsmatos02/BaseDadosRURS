package consultarResidentes;

/**
 * Esta tabela serve como agregacao para uma linha da tabela geral. Suporta um
 * maximo de 5 colunas
 * 
 * @author Joao Matos
 *
 */
public class TableRow {

	/**
	 * Coluna 1
	 */
	private String col1;

	/**
	 * Coluna 2
	 */
	private String col2;

	/**
	 * Coluna 3
	 */
	private String col3;

	/**
	 * Coluna 4
	 */
	private String col4;

	/**
	 * Coluna 5
	 */
	private String col5;

	/**
	 * @return a coluna 1
	 */
	public String getCol1() {
		return col1;
	}

	/**
	 * @param col1 - valor a colocar na coluna 1
	 */
	public void setCol1(String col1) {
		this.col1 = col1;
	}

	/**
	 * @return a coluna 2
	 */
	public String getCol2() {
		return col2;
	}

	/**
	 * @param col2 - valor a colocar na coluna 2
	 */
	public void setCol2(String col2) {
		this.col2 = col2;
	}

	/**
	 * @return a coluna 3
	 */
	public String getCol3() {
		return col3;
	}

	/**
	 * @param col3 - valor a colocar na coluna 3
	 */
	public void setCol3(String col3) {
		this.col3 = col3;
	}

	/**
	 * @return a coluna 4
	 */
	public String getCol4() {
		return col4;
	}

	/**
	 * @param col4 - valor a colocar na coluna 4
	 */
	public void setCol4(String col4) {
		this.col4 = col4;
	}

	/**
	 * @return a coluna 5
	 */
	public String getCol5() {
		return col5;
	}

	/**
	 * @param col5 - valor a colocar na coluna 5
	 */
	public void setCol5(String col5) {
		this.col5 = col5;
	}

}
